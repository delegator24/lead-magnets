 // ── что снимать первым ────────────────────────────────────────────────────
 function план(з) {
  var группы = {};
  з.forEach(function (x) {
   if (x.роль !== "Исполнитель" || !x.кому || x.кому === "Только я") return;
   var к = x.кому;
   (группы[к] = группы[к] || { часы: 0, задачи: [] });
   группы[к].часы += x.часы;
   if (x.задача && группы[к].задачи.indexOf(x.задача) < 0 && группы[к].задачи.length < 5) {
    группы[к].задачи.push(x.задача);
   }
  });
  return Object.keys(группы)
    .map(function (к) { return { кому: к, часы: группы[к].часы, задачи: группы[к].задачи }; })
    .sort(function (a, b) { return b.часы - a.часы; });
 }

 // ── свод ──────────────────────────────────────────────────────────────────
 function свести(з) {
  var с = {
   всего: 0, поМодулям: {}, поРолям: {}, поКому: {},
   исп: 0, утечка: 0, толькоЯ: 0, сРолью: 0, сКому: 0
  };
  МОДУЛИ.forEach(function (м) { с.поМодулям[м] = 0; });
  РОЛИ.forEach(function (р) { с.поРолям[р] = 0; });

  з.forEach(function (x) {
   с.всего += x.часы;
   с.поМодулям[x.модуль] = (с.поМодулям[x.модуль] || 0) + x.часы;
   if (x.роль) { с.поРолям[x.роль] = (с.поРолям[x.роль] || 0) + x.часы; с.сРолью += x.часы; }
   if (x.кому) { с.поКому[x.кому] = (с.поКому[x.кому] || 0) + x.часы; с.сКому += x.часы; }
   if (x.роль === "Исполнитель") {
    с.исп += x.часы;
    // главное: делает сам то, что есть кому отдать
    if (x.кому && x.кому !== "Только я") с.утечка += x.часы;
   }
   if (x.кому === "Только я") с.толькоЯ += x.часы;
  });

  с.доляИсп = с.всего ? с.исп / с.всего * 100 : 0;
  с.доляТолькоЯ = с.всего ? с.толькоЯ / с.всего * 100 : 0;
  // Насколько заполнены два главных столбца. Без них проценты занижены: пустая
  // роль неотличима от «не исполнитель». Молчать об этом нельзя — получится
  // вывод без данных.
  с.доляРолей = с.всего ? с.сРолью / с.всего * 100 : 0;
  с.доляКому = с.всего ? с.сКому / с.всего * 100 : 0;

  // сколько дней реально покрыто хронометражем
  var дни = {};
  з.forEach(function (x) { if (x.дата) дни[x.дата] = 1; });
  с.дни = Object.keys(дни).length;

  // спорные пометки и их вес в часах
  с.спорные = спорные(з);
  с.спорноЧасы = с.спорные.reduce(function (a, x) { return a + x.часы; }, 0);
  с.средняя = з.length ? с.всего / з.length : 0;
  с.модулиСорт = Object.keys(с.поМодулям)
    .map(function (м) { return [м, с.поМодулям[м]]; })
    .sort(function (a, b) { return b[1] - a[1]; });
  return с;
 }

