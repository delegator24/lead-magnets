 // ── зона ввода: вставка, перетаскивание, выбор файла ─────────────────────
 // Три способа, одно поле. Вставка — короче всего (Ctrl+C → Ctrl+V), но
 // заставлять не надо: кто-то работает в Excel локально и ему привычнее файл.
 function подсказка() {
  var есть = ($("input").value || "").trim().length > 0;
  $("drop").classList.toggle("has", есть);
 }

 async function принять(файл) {
  var ош = $("err"), метка = $("loaded");
  ош.classList.remove("on");
  try {
   var текст = await читатьФайл(файл);
   $("input").value = текст;
   подсказка();
   метка.textContent = "✓ " + файл.name;
   метка.classList.add("on");
   посчитать();
  } catch (e) {
   метка.classList.remove("on");
   ош.textContent = e.message || "Не смог прочитать файл";
   ош.classList.add("on");
  }
 }

 var зона = $("drop");
 if (зона) {
  $("input").addEventListener("input", function () {
   подсказка();
   $("loaded").classList.remove("on");
  });

  // перетаскивание: гасим стандартное поведение, иначе браузер откроет файл
  ["dragenter", "dragover"].forEach(function (с) {
   зона.addEventListener(с, function (e) {
    e.preventDefault(); e.stopPropagation();
    зона.classList.add("over");
   });
  });
  ["dragleave", "drop"].forEach(function (с) {
   зона.addEventListener(с, function (e) {
    e.preventDefault(); e.stopPropagation();
    // dragleave срабатывает и на дочерних узлах — проверяем, что ушли совсем
    if (с === "dragleave" && зона.contains(e.relatedTarget)) return;
    зона.classList.remove("over");
   });
  });
  зона.addEventListener("drop", function (e) {
   var ф = e.dataTransfer && e.dataTransfer.files && e.dataTransfer.files[0];
   if (ф) принять(ф);
  });

  $("pick").addEventListener("click", function () { $("file").click(); });
  $("file").addEventListener("change", function () {
   if (this.files[0]) принять(this.files[0]);
   this.value = "";                                   // чтобы тот же файл можно было выбрать снова
  });

  // вставили из таблицы — считаем сразу, кнопку жать не обязательно
  $("input").addEventListener("paste", function () {
   setTimeout(function () { подсказка(); посчитать(); }, 60);
  });
 }

